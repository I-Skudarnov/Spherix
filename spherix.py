def _build_bottom_surface():
    """Построить спрайт рельефа дна из изображения ref_bottom_new.png.

    Берём само эталонное изображение (а не рисуем дугами): вырезаем нижнюю
    коричневую полосу рельефа, убираем серую сетку и чёрный фон в прозрачность
    и масштабируем под ширину поля × высоту одного ряда. Тонкие вертикальные
    линии сетки, проходящие сквозь выступы, «замостим», чтобы они не оставляли
    тёмных щелей в коричневом рельефе.
    """
    global _bottom_surf
    from PIL import Image
    # Берём новый эталон (10 колонок); если его нет — пробуем старый.
    path = resource_path("ref_bottom_new.png")
    if not os.path.exists(path):
        path = resource_path("ref_bottom.png")
    # Нет файла — оставляем None, сработает запасная процедурная отрисовка.
    if not os.path.exists(path):
        _bottom_surf = None
        return
    img = Image.open(path).convert("RGBA")
    W, H = img.size
    px = img.load()

    # Признак «коричневого» пикселя рельефа (тёплый: R заметно больше G и B).
    def is_brown(r, g, b):
        return r > 120 and r > g + 20 and g > b and b < 150 and r - b > 40

    # Ищем верхнюю границу рельефа — первый сверху ряд с коричневым.
    top = H
    for y in range(H):
        for x in range(0, W, 3):
            r, g, b, a = px[x, y]
            if is_brown(r, g, b):
                top = y
                break
        if top != H:
            break

    # Вырезаем полосу рельефа от его верхней границы до низа картинки.
    strip = img.crop((0, top, W, H)).convert("RGBA")
    sw, sh = strip.size
    spx = strip.load()

    # 1) Строим маску «коричневых» пикселей рельефа.
    mask = [[1 if is_brown(*spx[x, y][:3]) else 0 for x in range(sw)]
            for y in range(sh)]
    # 2) «Замощаем» тонкие вертикальные щели сетки: пустой пиксель становится
    #    коричневым, если коричневое есть и слева, и справа в пределах GAP.
    #    Широкие впадины между выступами (десятки пикселей) при этом сохраняются.
    GAP = 3
    for y in range(sh):
        row = mask[y]
        filled = row[:]
        for x in range(sw):
            if row[x]:
                continue
            left  = any(row[x - k] for k in range(1, GAP + 1) if x - k >= 0)
            right = any(row[x + k] for k in range(1, GAP + 1) if x + k < sw)
            if left and right:
                filled[x] = 1
        mask[y] = filled

    # 3) Перекрашиваем по маске: всем пикселям один коричневый цвет, меняем лишь
    #    alpha (так при масштабировании по краям не возникает тёмной каймы).
    BROWN = (180, 100, 60)
    out = bytearray(sw * sh * 4)
    for y in range(sh):
        for x in range(sw):
            i = (y * sw + x) * 4
            out[i], out[i + 1], out[i + 2] = BROWN
            out[i + 3] = 255 if mask[y][x] else 0
    strip = Image.frombytes("RGBA", (sw, sh), bytes(out))

    # Масштабируем под игровое поле: ширина = GRID_COLS колонок, высота = ряд.
    strip = strip.resize((GRID_COLS * COL_W, ROW_H), Image.LANCZOS)
    _bottom_surf = pygame.image.fromstring(
        strip.tobytes(), strip.size, "RGBA").convert_alpha()

