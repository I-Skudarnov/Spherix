    # (80×64) располагался по центру окна под надписью NEXT.
    w, h = SIDEBAR_W - 8, 116
    x, y = 4, SCREEN_H - 130
    pygame.draw.rect(surf, BLUE, (x, y, w, h))
    pygame.draw.rect(surf, WHITE, (x, y, w, h), 1)
